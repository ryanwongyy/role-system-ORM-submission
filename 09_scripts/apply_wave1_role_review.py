def read_csv(path: Path) -> list[dict[str, str]]:
    with path.open("r", newline="", encoding="utf-8") as handle:
        return list(csv.DictReader(handle))


def write_csv(path: Path, fieldnames: list[str], rows: list[dict[str, str]]) -> None:
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)


def upsert_extract_rows(case_dir: Path, updates: list[dict[str, str]]) -> None:
    path = case_dir / "evidence_extracts.csv"
    rows = read_csv(path)
    by_id = {row["extract_id"]: row for row in rows}
    for update in updates:
        by_id[update["extract_id"]] = update
    ordered = [by_id[key] for key in sorted(by_id.keys())]
    write_csv(path, FIELDNAMES_EXTRACTS, ordered)


def update_role_rows(case_dir: Path, updates: dict[str, dict[str, str]]) -> None:
    path = case_dir / "role_coding.csv"
    rows = read_csv(path)
    fieldnames = list(rows[0].keys())
    for row in rows:
        if row["role_label"] in updates:
            row.update(updates[row["role_label"]])
    write_csv(path, fieldnames, rows)


def main() -> int:
    for case_id, payload in REVIEW_UPDATES.items():
        case_dir = PROJECT_ROOT / "02_cases" / payload["case_slug"]
        upsert_extract_rows(case_dir, payload["extracts"])
        update_role_rows(case_dir, payload["roles"])
    return 0


